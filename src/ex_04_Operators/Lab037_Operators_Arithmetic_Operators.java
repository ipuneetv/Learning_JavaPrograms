package ex_04_Operators;

public class Lab037_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // result is 6 since both are integer
        System.out.println(a/c); // result is 6.666665, since we devided a with float
        System.out.println(a%b);
        System.out.println("a+b");// will print as it is
        System.out.println(a+b);






    }
}
